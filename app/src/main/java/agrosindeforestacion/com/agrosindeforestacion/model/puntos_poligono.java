public class puntos_poligono {
    public  String id_puntos_poligono;
    public String latitud;
    public  String longitud;

    public String getId_puntos_poligono() {
        return id_puntos_poligono;
    }

    public void setId_puntos_poligono(String id_puntos_poligono) {
        this.id_puntos_poligono = id_puntos_poligono;
    }

    public String getLatitud() {
        return latitud;
    }

    public void setLatitud(String latitud) {
        this.latitud = latitud;
    }

    public String getLongitud() {
        return longitud;
    }

    public void setLongitud(String longitud) {
        this.longitud = longitud;
    }
}
